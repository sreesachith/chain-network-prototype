#!/bin/bash

function createrr() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/rr.isfcr.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/rr.isfcr.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-rr --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-rr.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-rr.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-rr.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-rr.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/rr.isfcr.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy rr's CA cert to rr's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/rr.isfcr.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/rr/ca-cert.pem" "${PWD}/organizations/peerOrganizations/rr.isfcr.com/msp/tlscacerts/ca.crt"

  # Copy rr's CA cert to rr's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/rr.isfcr.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/rr/ca-cert.pem" "${PWD}/organizations/peerOrganizations/rr.isfcr.com/tlsca/tlsca.rr.isfcr.com-cert.pem"

  # Copy rr's CA cert to rr's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/rr.isfcr.com/ca"
  cp "${PWD}/organizations/fabric-ca/rr/ca-cert.pem" "${PWD}/organizations/peerOrganizations/rr.isfcr.com/ca/ca.rr.isfcr.com-cert.pem"

# Peer Registration ---------------------------------------
  infoln "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-rr --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  #This is thr new peers that are getting registered into the network
  infoln "Registering peer1"
  set -x
  fabric-ca-client register --caname ca-rr --id.name peer1 --id.secret peer1pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering peer2"
  set -x
  fabric-ca-client register --caname ca-rr --id.name peer2 --id.secret peer2pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

#---------------------------------------------
  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-rr --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-rr --id.name rradmin --id.secret rradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-rr -M "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/msp/config.yaml"

  infoln "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-rr -M "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls" --enrollment.profile tls --csr.hosts peer0.rr.isfcr.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer0.rr.isfcr.com/tls/server.key"


  infoln "Generating the peer1 msp"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-rr -M "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/msp/config.yaml"

