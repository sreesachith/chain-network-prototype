  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer1.rr.isfcr.com/tls/server.key"
  
  
  infoln "Generating the peer2 msp"
  set -x
  fabric-ca-client enroll -u https://peer2:peer2pw@localhost:7054 --caname ca-rr -M "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer2.rr.isfcr.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/rr/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/rr.isfcr.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/rr.isfcr.com/peers/peer2.rr.isfcr.com/msp/config.yaml"

